/**
 * static
 * 
 * static is a keyword which usually gets attched with variables, functions
 * 
 * any static function/variable belongs to the Class instead object.
 *      if we want to use static function/variable in/outside the Class, we have to use ClassName.
 *      if a static variable is changed then it's changed for ALL objects (all objects shares the static-variables).
 * 
 * 
 * any non-static function/variable belongs to the object.
 * 
 * 
 * When we want a variable to be shared between all object
 *      we make it as a static-variable.
 * 
 * When we want a function to be performed(or executed) by CLass or if a fucntion is helping function
 *      we make it as a static-function.
 * 
 */